山西大学自行车协会，英文名CASU。社团成立于1998年11月18日，是山西大学成立最早的一批社团之一。自行车协会是在校团委领导下，由热爱自行车运动，勇于挑战自我，深入社会的在校大学生自发组织，自愿参加的学生社团。

